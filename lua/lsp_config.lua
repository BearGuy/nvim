            on_init = function(client)
                client.config.settings.python.pythonPath = get_python_path(client.config.root_dir)
            end
        }
    elseif(lsp == 'elixirls')
    then
        nvim_lsp[lsp].setup{
            cmd = { "/Users/stephenpeterkins/.config/language_servers/elixir-ls/language_server.sh" };
        }
    else
        nvim_lsp[lsp].setup{}
    end
end

